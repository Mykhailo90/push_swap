#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t len)
{
	char	*sp;

	sp = s1;
	while (*sp)
		sp++;
	while (len > 0 && *s2 != '\0')
	{
		*sp++ = *s2++;
		--len;
	}
	*sp++ = '\0';
	return (s1);
}
