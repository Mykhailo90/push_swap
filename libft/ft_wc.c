#include "libft.h"

size_t	ft_wc(char *s, char c)
{
	size_t n;
	size_t i;

	n = 0;
	i = 0;
	while (s[n] != '\0')
	{
		if (n == 0 && s[n] != c)
			i++;
		if (s[n] == c && s[n + 1] != c && s[n + 1] != '\0')
			i++;
		n++;
	}
	return (i);
}
